    idioma_pred = []
    idiomas_orden = ['Ingles','Español','Aleman','Italiano']
    idiomas = [Ing,Esp,Ale,Ita]
    for x in idiomas:
        aciertos.append([abs(texto_frecuencias[z] - x[z]) for z in texto_frecuencias])
